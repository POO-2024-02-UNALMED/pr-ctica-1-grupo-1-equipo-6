
public class Adiciones {
    private int refrigerios;
    private int transporte;
    private int hotel;
    private int plantar_arbol;
    private int advisar_ave;
    private int casa_cultura;
    private int cupon;
    private int descuento;

    public int getPlantar_arbol() {
        return plantar_arbol;
    }
    public void setPlantar_arbol(int plantar_arbol) {
        this.plantar_arbol = plantar_arbol;
    }

    public int getAdvisar_ave() {
        return advisar_ave;
    }
    public void setAdvisar_ave(int advisar_ave) {
        this.advisar_ave = advisar_ave;
    }

    public int getCasa_cultura() {
        return casa_cultura;
    }

    public void setCasa_cultura(int casa_cultura) {
        this.casa_cultura = casa_cultura;
    }

    public int getCupon() {
        return cupon;
    }

    public void setCupon(int cupon) {
        this.cupon = cupon;
    }

    public int getDescuento() {
        return descuento;
    }

    public void setDescuento(int descuento) {
        this.descuento = descuento;
    }

    public int getRefrigerios() {
        return refrigerios;
    }
    public void setRefrigerios(int refrigerios) {
        this.refrigerios = refrigerios;
    }

    public int getTransporte() {
        return transporte;
    }
    public void setTransporte(int transporte) {
        this.transporte = transporte;
    }

    public int getHotel() {
        return hotel;
    }
    public void setHotel(int hotel) {
        this.hotel = hotel;
    }
}
